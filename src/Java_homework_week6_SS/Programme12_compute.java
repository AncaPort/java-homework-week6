package Java_homework_week6_SS;

public class Programme12_compute {
    //Write a Java program to compute the specified expressions and print output.

    public static void main(String[] args) {
        double num1 = 25.5;
        double num2 = 3.5;
        double num3 = 40.5;
        double num4 = 4.5;

        double result = ((num1 * num2 - num2 * num2) / (num3 - num4));

        System.out.println("The result of the expression is: " + result);
    }

}
